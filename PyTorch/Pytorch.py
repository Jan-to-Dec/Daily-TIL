# 1. torch:
# 파이토치의 메인 네임스페이스입니다.
# 텐서(Tensor)를 포함한 다양한 수학 함수를 제공하며, 넘파이(NumPy)와 유사한 구조를 가지고 있습니다.
# 텐서 연산, 선형 대수 연산, 통계 기능 등을 수행하는 데 사용됩니다.

# 2. torch.autograd:
# 자동 미분 기능을 제공합니다.
# 컴퓨터 그래프(Computational Graph)를 자동으로 구축하여 미분을 수행하기 때문에 코드 작성이 간편합니다.
# 학습 과정에서 손실 함수의 기울기를 계산하는데 활용됩니다.

# 3. torch.nn:
# 신경망 구축을 위한 다양한 데이터 구조와 레이어를 제공합니다.
# 선형층, 컨볼루션층, 풀링층, RNN/LSTM 레이어 등 다양한 레이어를 포함합니다.
# ReLU, sigmoid, softmax와 같은 활성화 함수와 MSE, CrossEntropy 등 손실 함수도 제공합니다.

# 4. torch.optim:
# 확률적 경사 하강법(SGD)을 포함한 다양한 파라미터 최적화 알고리즘을 제공합니다.
# Adam, RMSprop, AdamW 등 다양한 최적화 알고리즘을 선택하여 사용할 수 있습니다.
# 학습 과정에서 모델의 가중치를 자동으로 업데이트하는데 사용됩니다.

# 5. torch.utils.data:
# 미니배치 기반 학습을 위한 데이터 처리 및 로딩 기능을 제공합니다.
# DataLoader 클래스를 통해 데이터셋을 효율적으로 로딩하고 미니배치로 처리할 수 있습니다.
# 배치 샘플링, 데이터 증강, 셔플링 등 다양한 기능을 제공합니다.

# 6. torch.onnx:
# ONNX(Open Neural Network Exchange) 포맷으로 모델을 내보내는 기능을 제공합니다.
# ONNX는 다양한 딥러닝 프레임워크 간 모델 공유를 가능하게 하는 표준 포맷입니다.
# 다른 프레임워크에서도 개발된 모델을 불러와 실행하거나 분석하는데 활용됩니다.