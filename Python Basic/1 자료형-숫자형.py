# 정수형 
a = 10
b = 5

# 기본적인 산술 연산
print(a + b)  # 더하기
print(a - b)  # 빼기
print(a * b)  # 곱하기
print(a // b) # 나누기(정수 나눗셈)
print(a % b)  # 나머지
print(a ** b) # 거듭제곱


# float
x = 10.5
y = 4.2

print(x + y)  # 더하기
print(x - y)  # 빼기
print(x * y)  # 곱하기
print(x / y)  # 나누기
print(x ** y) # 거듭제곱

